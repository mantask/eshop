<?php

namespace AppBundle\Util;

use Doctrine\ORM\Mapping as ORM;

trait IdentifiableEntityTrait
{
    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     *
     * @var int
     */
    protected $id;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }
}
